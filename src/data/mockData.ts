export function getRequirementForTest(testItemId: number, direction?: string): TestRequirement | undefined {
  return mockRequirements.find(r => r.testItemId === testItemId && (!direction || r.direction === direction));
}

export function autoJudge(value: number | null, req?: TestRequirement): Judgment {
  if (value === null || !req) return 'Pending';
  if (req.minValue !== undefined && value < req.minValue) return 'NG';
  if (req.maxValue !== undefined && value > req.maxValue) return 'NG';
  return 'OK';
}
